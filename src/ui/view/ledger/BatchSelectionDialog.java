    private void initTable() {
        tableModel = (DefaultTableModel) itemDataTable.getModel();
        addAllBatchesToTable();
    }

    private void addAllBatchesToTable() {
        tableModel.setNumRows(0);
        try {
            ArrayList<Batch> bl = BatchController.getAllAvailableBatchesOfGrn(this.txtGrnNumber.getText());
            if (bl == null) {
                return;
            }
            for (Batch b : bl) {
                Product p = ProductController.getProduct(b.getProductId());
                tableModel.addRow(new Object[]{b, p.getProductName(), b.getBatchId()});
            }
            DBConnection.closeConnectionToDB();
        } catch (SQLException ex) {
            logger.error("Error: " + ex.getMessage());
        }
    }

    private void initInterface() {
        tableModel = (DefaultTableModel) itemDataTable.getModel();
        addAllBatchesToTable();
    }

    private void addSelectedBatch() {
        int row = itemDataTable.getSelectedRow();
        if (row >= 0) {
            Batch b = (Batch) tableModel.getValueAt(row, 0);
            parent.batchSelectionResponce(b);
        }
    }

    public void disableGRNSelection(String text) {
        txtGrnNumber.setText(text);
        addAllBatchesToTable();
        txtGrnNumber.setEditable(false);
    }

    void centerOnDesktop() {
        this.setLocation(new Point(((JInternalFrame) parent).getWidth() / 2 + ((JInternalFrame) parent).getX() - this.getWidth() / 2, ((JInternalFrame) parent).getHeight() / 2 + ((JInternalFrame) parent).getY() - this.getHeight() / 2));
    }

    private void cancel() {
        this.dispose();
    }
